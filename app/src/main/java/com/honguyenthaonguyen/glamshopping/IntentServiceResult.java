public class IntentServiceResult {

    int mResult;
    String mResultValue;

    public IntentServiceResult(int mResult, String mResultValue) {
        this.mResult = mResult;
        this.mResultValue = mResultValue;
    }

    public int getResult() {
        return mResult;
    }

    public String getResultValue() {
        return mResultValue;
    }



}
